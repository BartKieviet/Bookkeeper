var IDS_BY_NAME; // lazily initialized in Sectors.getId

// `data` is the sector data as per Sector.getData().  External code will most
// likely want to use the conveniences Sector.createFromId() and
// Sector.createFromName().

function Sector( data ) {
	this.name = data[ 0 ];
	this.height = data[ 1 ];
	this.locbase = data[ 2 ];
}

Sector.getId = function( sector_name ) {
	if( !IDS_BY_NAME ) {
		var i, end;
		IDS_BY_NAME = {};
		for( i = 0, end = SECTORS.length; i < end; i++ ) {
			IDS_BY_NAME[ SECTORS[i][0] ] = i + 1;
		}
	}

	return IDS_BY_NAME[ sector_name ] || undefined;
}

Sector.getData = function( sector_id ) {
	return SECTORS[ sector_id - 1 ] || undefined;
}

Sector.getName = function( sector_id ) {
	var d = Sector.getData( sector_id );
	return d ? d[0] : undefined;
}

Sector.createFromId = function( id ) {
	var data = Sector.getData( id );
	return data ? new Sector( data ) : null;
}

Sector.createFromName = function( name ) {
	var id = Sector.getId( name );
	return id ? Sector.createFromId(id) : null;
}

Sector.prototype.getId = function() {
	return Sector.getId( this.name );
}

Sector.prototype.getLocation = function( x, y ) {
	return this.locbase + this.height*x + y;
}

Sector.prototype.getCoords = function( loc ) {
	loc -= this.locbase;
	return { x: Math.floor( loc / this.height ), y: loc % this.height };
}

return Sector;

