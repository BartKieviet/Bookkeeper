var SECTOR_IDS; // lazily initialized in Sector.getId

var Sector = {};

Sector.getId = function( sector_name ) {
	if( !SECTOR_IDS ) {
		var i, end;
		SECTOR_IDS = {};
		for( i = 0, end = SECTORS.length; i < end; i++ ) {
			SECTOR_IDS[ SECTORS[i] ] = i + 1;
		}
	}

	return SECTOR_IDS[ sector_name ] || undefined;
}

Sector.getName = function( sector_id ) {
	return SECTORS[ sector_id-1 ] || undefined;
}

